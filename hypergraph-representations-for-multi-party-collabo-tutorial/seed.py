    # TASK: 'Design agent communication protocols' - Bob, Alice (hyperedge!)
    task = tasks_by_title["Design agent communication protocols"]
    for agent_name in ["Bob Patel", "Alice Chen"]:
        agent = agents_by_name[agent_name]
