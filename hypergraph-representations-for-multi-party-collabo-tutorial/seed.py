def create_relationships(
    db: RushDB,
    agents: list,
    projects: list,
    tasks: list,
    artifacts: list
) -> None:
    """
    Create hyperedge relationships modeling multi-party collaboration.
    
    Key hypergraph patterns:
    - Tasks assigned to multiple agents (COLLABORATES_ON)
    - Agents contributing to multiple projects (WORKS_ON)
    - Tasks producing multiple artifacts (PRODUCES)
    - Projects containing multiple tasks (CONTAINS)
    - Agents reviewing multiple artifacts (REVIEWED_BY)
    """
    print("  [5/5] Creating collaboration relationships...")
    
    # Index records by name for easy lookup
    agents_by_name = {a["name"]: a for a in agents}
    projects_by_name = {p["name"]: p for p in projects}
    tasks_by_title = {t["title"]: t for t in tasks}
    artifacts_by_title = {a["title"]: a for a in artifacts}
    
    relationship_count = 0
    
    # TASK: 'Research semantic search architectures' - Alice only
    task = tasks_by_title["Research semantic search architectures"]
    agent = agents_by_name["Alice Chen"]
    db.records.attach(source=agent, target=task, options={"type": "COLLABORATES_ON", "direction": "out"})
    relationship_count += 1
    
    # TASK: 'Implement semantic search algorithm' - Alice, David, Emma (hyperedge!)
    task = tasks_by_title["Implement semantic search algorithm"]
    for agent_name in ["Alice Chen", "David Kim", "Emma Zhang"]:
        agent = agents_by_name[agent_name]
        db.records.attach(source=agent, target=task, options={"type": "COLLABORATES_ON", "direction": "out"})
        relationship_count += 1
    
    # TASK: 'Design system architecture' - Bob only
    task = tasks_by_title["Design system architecture"]
    agent = agents_by_name["Bob Patel"]
    db.records.attach(source=agent, target=task, options={"type": "COLLABORATES_ON", "direction": "out"})
    relationship_count += 1
    
    # TASK: 'Literature review on context compression' - Alice only
    task = tasks_by_title["Literature review on context compression"]
    agent = agents_by_name["Alice Chen"]
    db.records.attach(source=agent, target=task, options={"type": "COLLABORATES_ON", "direction": "out"})
    relationship_count += 1
    
    # TASK: 'Implement compression baseline' - David, Carol (hyperedge!)
    task = tasks_by_title["Implement compression baseline"]
    for agent_name in ["David Kim", "Carol Williams"]:
        agent = agents_by_name[agent_name]
        db.records.attach(source=agent, target=task, options={"type": "COLLABORATES_ON", "direction": "out"})
